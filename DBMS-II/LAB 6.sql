SET STUPHONE = '9898'
WHERE STUDENTID = 2;

SELECT * FROM STUDENT;

DROP TRIGGER TRG_UPDATE_STUDENT_PHONE;

--10. Create trigger for updating course credit log old and new credits in log table.
CREATE OR ALTER TRIGGER TRG_UPDATE_COURSE_CREDIT
ON COURSE
AFTER UPDATE
AS
    BEGIN
        INSERT INTO LOG(LOGMESSAGE, LOGDATE)
        SELECT 'COURSE ' + CAST(I.COURSEID AS VARCHAR) +
               ' CREDIT CHANGED FROM ' + CAST(D.COURSECREDITS AS VARCHAR) +
               ' TO ' + CAST(I.COURSECREDITS AS VARCHAR),
               GETDATE()
        FROM DELETED D, INSERTED I
        WHERE D.COURSEID = I.COURSEID;
    END


