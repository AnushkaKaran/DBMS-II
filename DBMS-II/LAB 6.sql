--9. Create trigger for updating student phone and print the old and new phone number. 
CREATE OR ALTER TRIGGER TRG_UPDATE_STUDENT_PHONE
ON STUDENT
AFTER UPDATE
AS
BEGIN
        SELECT 'OLD PHONE: ' + CAST(DELETED.STUPHONE AS VARCHAR) +
               ' NEW PHONE: ' + CAST(INSERTED.STUPHONE AS VARCHAR)
        FROM DELETED, INSERTED
        WHERE DELETED.STUDENTID = INSERTED.STUDENTID;
END;

