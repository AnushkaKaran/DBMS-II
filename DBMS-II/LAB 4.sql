--4. Write a scalar function which returns the sum of Credits for two given CourseIDs.
CREATE OR ALTER FUNCTION FN_SUMOF_CREADTIS(@ID1 VARCHAR(10), @ID2 VARCHAR(10))
RETURNS INT
AS
BEGIN
	DECLARE @SUM INT
	SET @SUM = (SELECT COURSECREDITS FROM COURSE WHERE COURSEID=@ID1) + (SELECT COURSECREDITS FROM COURSE WHERE COURSEID=@ID2)
	RETURN @SUM
END

