--6. Write a function to print number from 1 to N. (Using while loop)
CREATE OR ALTER FUNCTION FN_PRINT_1TON(@NUMBER INT)
RETURNS VARCHAR(200)
AS
BEGIN
	DECLARE @RESULT VARCHAR(200),@COUNT INT
	SET @RESULT = ''
	SET @COUNT=1
	WHILE(@COUNT<=@NUMBER)
	BEGIN
		SET @RESULT = @RESULT+ ' '+CAST(@COUNT AS VARCHAR)
		SET @COUNT = @COUNT+1 
	END
	RETURN @RESULT
END

SELECT DBO.FN_PRINT_1TON(5)

--7. Write a scalar function to calculate factorial of total credits for a given CourseID.
CREATE OR ALTER FUNCTION FN_FAC_CREDITS(@ID VARCHAR(10))
RETURNS INT
AS
BEGIN
	DECLARE @CREDITS INT;
	DECLARE @FACTORIAL INT = 1;
	DECLARE @I INT = 1;

	SET @CREDITS = (SELECT COURSECREDITS FROM COURSE WHERE COURSEID = @ID)

	WHILE (@I<=@CREDITS)
	BEGIN
		SET @FACTORIAL = @FACTORIAL*@I
		SET @I = @I+1
	END
	RETURN @FACTORIAL 
END

SELECT DBO.FN_FAC_CREDITS('IT201');

--8. Write a scalar function to check whether a given EnrollmentYear is in the past, current or future (Case
--statement)
CREATE OR ALTER FUNCTION FN_ENROLLMENTYEAR(@YEAR INT)
RETURNS VARCHAR(20)
AS
BEGIN
	DECLARE @RESULT VARCHAR(20)
	RETURN CASE 
		WHEN @YEAR<YEAR(GETDATE()) THEN 'PAST YEAR'
		WHEN @YEAR>YEAR(GETDATE()) THEN 'FUTURE'
		ELSE 'CURRENT YEAR'
	END
END

SELECT DBO.FN_ENROLLMENTYEAR(2003);

