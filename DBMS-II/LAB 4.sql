CREATE OR ALTER FUNCTION FN_SIMPLEINTEREST(
	@PRICE FLOAT,
	@RATE FLOAT,
	@TIME FLOAT
)
RETURNS FLOAT
AS
BEGIN
	RETURN (@PRICE*@RATE*@TIME)/100;
END

